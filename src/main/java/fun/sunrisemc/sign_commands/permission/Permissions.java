package fun.sunrisemc.sign_commands.permission;

public class Permissions {

    // SignCommands User Permissions
    public static final String USE_PERMISSION = "signcommands.use"; // Allows the player to use command signs.

    // SignCommands Admin Permissions
    public static final String RELOAD_PERMISSION = "signcommands.admin.reload"; // Allows the player to reload the SignCommands configuration.
    public static final String INFO_PERMISSION = "signcommands.admin.info"; // Allows the player to view information about a command sign.
    public static final String LIST_SIGNS_PERMISSION = "signcommands.admin.list"; // Allows the player to list all command signs.
    public static final String GOTO_SIGN_PERMISSION = "signcommands.admin.goto"; // Allows the player to teleport to a command sign.
    public static final String RENAME_SIGN_PERMISSION = "signcommands.admin.rename"; // Allows the player to rename a command sign.
    public static final String DELETE_SIGN_PERMISSION = "signcommands.admin.delete"; // Allows the player to delete a command sign.
    public static final String MANAGE_COMMANDS_PERMISSION = "signcommands.admin.commands"; // Allows the player to manage commands on command signs.
    public static final String MANAGE_PERMISSIONS_PERMISSION = "signcommands.admin.permissions"; // Allows the player to manage permissions on command signs.
    public static final String MANAGE_GLOBAL_CLICK_COOLDOWN_PERMISSION = "signcommands.admin.globalclickcooldown"; // Allows the player to manage the global click cooldown on command signs.
    public static final String MANAGE_GLOBAL_CLICK_LIMIT_PERMSSION = "signcommands.admin.globalmaxclicks"; // Allows the player to manage the global max clicks on command signs.
    public static final String MANAGE_USER_CLICK_COOLDOWN_PERMISSION = "signcommands.admin.userclickcooldown"; // Allows the player to manage the user click cooldown on command signs.
    public static final String MANAGE_USER_CLICK_LIMIT_PERMISSION = "signcommands.admin.usermaxclicks"; // Allows the player to manage the user max clicks on command signs.
    public static final String MANAGE_CLICK_COST_PERMISSION = "signcommands.admin.clickcost"; // Allows the player to manage the click cost on command signs.

    // Sign Edit Permissions
    public static final String SIGN_EDIT_USE_PERMISSION = "signedit.use"; // Allows the player to use the sign edit command.
    public static final String SIGN_EDIT_COLOR_PERMISSION = "signedit.color"; // Allows use of color codes on signs.
    public static final String BYPASS_PROTECTIONS_PERMISSION = "signedit.bypassprotections"; // Allows bypassing protections when editing signs.
    
}